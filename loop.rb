# 0から30までのうち、3の倍数のみ出力してください
i = 0
for i in 0...30 do
    i += 1
    if i % 3 == 0  # %を使って余りを計算して0だったら（=割り切れたら）
        puts i    #真の場合の処理（今回は偽の場合の処理を定義しないのでelse以降を書く必要がない）
    end  # 条件分岐処理を始めた構文の末尾に必ずendを付す
end

