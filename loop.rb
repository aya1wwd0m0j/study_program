#繰り返し構文

#1から10まで数字を出力する
#puts 1  #これを1から10まで自動で出したい
# パターン1
i = 0   # i = integer(数字)を意味する使い方（慣習）
for i in 0..10 do  # i = integer(数字)を意味する使い方（慣習）
    i += 1  #iに1を足して、iとして返す（変数に上書き処理する）
    puts i  #上で計算した結果を出力する
end

